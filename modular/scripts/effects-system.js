// ==================== 1. 烟花粒子系统 ====================

/**
 * 完整的粒子烟花模拟器
 * 
 * 支持 10+ 种烟花弹型：
 * - chrysanthemum (菊花)    — 经典球形爆发
 * - ghost (幽灵)           — 淡出消散效果
 * - strobe (频闪)          — 闪烁效果
 * - palm (棕榈)            — 棕榈树形状
 * - ring (圆环)            — 环形爆发
 * - crossette (十字)       — 交叉分裂
 * - floral (花束)          — 花朵形状
 * - willow (垂柳)          — 下垂尾迹
 * - crackle (爆竹)         — 二次爆炸
 * - horsetail (马尾)       — 长尾焰
 * 
 * 音效管理：
 * - burst (爆炸声)
 * - launch (发射声)
 * - crackle (噼啪声)
 * 
 * 用途：年度报告的"烟花祝福页"，展示用户的年度关键词
 */

